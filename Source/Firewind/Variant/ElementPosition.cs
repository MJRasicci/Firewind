namespace Firewind.Variant;

/// <summary>
/// Represents directional placement options for component elements.
/// </summary>
public enum ElementPosition
{
    /// <summary>
    /// No positional modifier.
    /// </summary>
    None,
    /// <summary>
    /// Positions at the logical start.
    /// </summary>
    Start,
    /// <summary>
    /// Positions at the logical end.
    /// </summary>
    End,
    /// <summary>
    /// Positions at the left side.
    /// </summary>
    Left,
    /// <summary>
    /// Positions at the top.
    /// </summary>
    Top,
    /// <summary>
    /// Positions at the right side.
    /// </summary>
    Right,
    /// <summary>
    /// Positions at the bottom.
    /// </summary>
    Bottom
}
