namespace Firewind.Variant;

/// <summary>
/// Represents semantic theme color tokens used by Firewind.Variants.
/// </summary>
public enum ThemeColor
{
    /// <summary>
    /// No theme color modifier.
    /// </summary>
    None,
    /// <summary>
    /// Neutral color token.
    /// </summary>
    Neutral,
    /// <summary>
    /// Primary color token.
    /// </summary>
    Primary,
    /// <summary>
    /// Secondary color token.
    /// </summary>
    Secondary,
    /// <summary>
    /// Accent color token.
    /// </summary>
    Accent,
    /// <summary>
    /// Informational color token.
    /// </summary>
    Info,
    /// <summary>
    /// Success color token.
    /// </summary>
    Success,
    /// <summary>
    /// Warning color token.
    /// </summary>
    Warning,
    /// <summary>
    /// Error color token.
    /// </summary>
    Error,
    /// <summary>
    /// Base surface color 100.
    /// </summary>
    Base100,
    /// <summary>
    /// Base surface color 200.
    /// </summary>
    Base200,
    /// <summary>
    /// Base surface color 300.
    /// </summary>
    Base300,
}
