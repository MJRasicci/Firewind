namespace Firewind.Style;

/// <summary>
/// Represents standard size options used by Firewind components.
/// </summary>
public enum ComponentSize
{
    /// <summary>
    /// Extra small size.
    /// </summary>
    Tiny = -2,
    /// <summary>
    /// Small size.
    /// </summary>
    Small = -1,
    /// <summary>
    /// Default size.
    /// </summary>
    Normal = 0,
    /// <summary>
    /// Large size.
    /// </summary>
    Large = 1,
    /// <summary>
    /// Responsive size that adapts across breakpoints.
    /// </summary>
    Responsive = 2
}
