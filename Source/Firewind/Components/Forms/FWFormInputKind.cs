namespace Firewind.Components;

/// <summary>
/// Represents a supported field input control kind.
/// </summary>
public enum FWFormInputKind
{
    /// <summary>
    /// Text input.
    /// </summary>
    Text,

    /// <summary>
    /// Numeric input.
    /// </summary>
    Number,

    /// <summary>
    /// Boolean checkbox.
    /// </summary>
    Checkbox,

    /// <summary>
    /// Date input.
    /// </summary>
    Date,

    /// <summary>
    /// Multi-line text area.
    /// </summary>
    TextArea,

    /// <summary>
    /// Select list.
    /// </summary>
    Select
}
